create database Sup_Chain

-- 1- Products Table (Stores unique product info)

CREATE TABLE Products (
    SKU VARCHAR(50) PRIMARY KEY,
    p_type VARCHAR(50),
    Price DECIMAL(10,2),
    avail_per INT,
    stock_per INT
	)

-- 2- Customers Table (Captures customer-related attributes)

CREATE TABLE Customers (
    CustID INT PRIMARY KEY,
    gender VARCHAR(25)
	)

-- 3 - Orders Table (Stores product sales and revenue details)

CREATE TABLE Orders (
    ord_ID INT PRIMARY KEY,
    SKU VARCHAR(50),
	cust_ID INT,
    no_prod_sold INT,
    rev_gen DECIMAL(12,2),
    order_qun INT,
    customer_lt INT,
    FOREIGN KEY (SKU) REFERENCES Products(SKU),
	FOREIGN KEY (cust_ID) REFERENCES Customers (CustID)
	)

-- 4- Suppliers Table (Includes supplier details)

CREATE TABLE Suppliers (
    SupID INT PRIMARY KEY,
    sup_name VARCHAR(25),
    delv_location VARCHAR(25),
    Supplier_lt INT
	)


-- 5- Manufacturing Table (Includes production details)

CREATE TABLE Manufacturing (
    manufuct_Id INT PRIMARY KEY,
    SKU VARCHAR(50),
    SupID INT,
    prod_volum INT,
    manfuct_lt INT,
    manfuct_cost DECIMAL(12,2),
    inspect_resl VARCHAR(50),
    def_rate DECIMAL(5,2),
    FOREIGN KEY (SKU) REFERENCES Products(SKU),
    FOREIGN KEY (SupID) REFERENCES Suppliers(SupID)
	)

-- 6- Shipping Table (Manage transportation details, track inspections, and monitor defect rates)

CREATE TABLE Shipping (
    Shipping_Id INT PRIMARY KEY,
    SKU VARCHAR(50),
    SupID INT,
    shipping_lt INT,
    shipping_carr VARCHAR(25),
    shipping_cost DECIMAL(12,2),
    trans_modes VARCHAR(25),
    Route VARCHAR(25),
    other_cost DECIMAL(12,2),
    FOREIGN KEY (SKU) REFERENCES Products(SKU),
    FOREIGN KEY (SupID) REFERENCES Suppliers(SupID)
	)

